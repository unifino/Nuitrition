// eslint-disable-next-line
export const MainTransition = async function ( phase: "In"|"Out", [X100,X010]: any ) { 
    switch ( phase ) {
// -- =====================================================================================
        case "In":
            await new Promise( _ => setTimeout( _, 900 + 860 ) )
            X100.value.className = "x_xxx " + "fallIn_X100"
            X010.value.className = "x_xxx " + "fallIn_X010"
            break;
// -- =====================================================================================
        case "Out":
            X100.value.className = "x_xxx " + "fallOut_X100"
            X010.value.className = "x_xxx " + "fallOut_X010"
            break;
// -- =====================================================================================
    }
}

